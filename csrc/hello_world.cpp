#include <iostream>

// The main function is where program execution begins.
int main() {
    // std::cout is used to output data to the console (standard output).
    // The << operator sends the string "Hello, World!" to the output stream.
    // std::endl (End Line) inserts a newline character and flushes the buffer.
    std::cout << "Hello, World!" << std::endl;

    // The program returns 0 to the operating system,
    // indicating successful execution.
    return 0;
}
